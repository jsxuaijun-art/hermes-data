def generate_all():
    """生成所有文书到桌面"""
    os.makedirs(OUTPUT_DIR, exist_ok=True)
    
    stages = {
        "01_客户接洽": [
            "客户资质审查表",
            "保密协议",
            "风险评估表（C-02）",
        ],
        "02_业务签约": [
            "代理记账业务委托合同",
            "代理记账服务清单勾选表",
            "代理记账业务工作计划",
            "业务承接与保持评价表",
        ],
        "03_资料交接": [
            "资料交接手册",
            "原始凭证交接表",
            "客户资料完整性确认书",
            "客户信息登记表（C-01）",
        ],
        "04_账务处理": [
            "日常记账审核表",
            "重点科目复核记录",
            "特殊业务处理台账",
            "科目异常调整记录（O-02）",
        ],
        "05_税务申报": [
            "税务申报差异说明表",
            "三表比对记录",
        ],
        "06_客户服务": [
            "财税健康诊断报告",
            "投诉处理记录",
        ],
        "07_业务终止": [
            "交接清单（业务终止）",
            "会计档案移交清册",
        ],
        "08_质量管理": [
            "月度自查表（Q-01）",
            "监管检查预审表（Q-02）",
            "岗位权限表",
            "培训考核记录",
        ],
    }
    
    generated_count = 0
    for stage, doc_names in stages.items():
        stage_dir = os.path.join(OUTPUT_DIR, stage)
        os.makedirs(stage_dir, exist_ok=True)
        for doc_name in doc_names:
            if doc_name in TEMPLATES:
                filepath = os.path.join(stage_dir, doc_name + ".txt")
                with open(filepath, "w", encoding="utf-8") as f:
                    f.write(TEMPLATES[doc_name])
                generated_count += 1
                print(f"  OK {stage}/{doc_name}")
            else:
                print(f"  XX 未找到模板: {doc_name}")
    
    print(f"\n======== 生成完成！共 {generated_count} 份文书 ========")
    print(f"保存路径: {OUTPUT_DIR}")
    print(f"\n目录结构：")
    for stage in stages:
        print(f"    {stage}/")
        for doc in stages[stage]:
            print(f"      {doc}.txt")


